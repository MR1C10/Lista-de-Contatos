/*
 
Lista de Contatos

Descrição:
Desenvolva uma aplicação de console em C# para gerenciar uma lista de contatos. A aplicação deve permitir ao usuário realizar operações básicas como adicionar, listar, buscar, editar e excluir contatos.

Requisitos Funcionais:

Adicionar Contato: FEITO

O usuário deve informar o nome e o telefone do contato.
Não devem ser permitidos contatos com o mesmo nome.

Listar Contatos: FEITO

A aplicação deve exibir uma lista de todos os contatos cadastrados, mostrando nome e telefone.
Exibir uma mensagem apropriada se não houver contatos cadastrados.

Buscar Contato: EM ANDAMENTO

O usuário deve poder pesquisar um contato pelo nome.
Exibir uma mensagem apropriada se o contato não for encontrado.

Editar Contato:

O usuário deve poder alterar o nome e/ou telefone de um contato existente.
Se o contato não existir, exibir uma mensagem informando isso.

Excluir Contato:

O usuário deve poder remover um contato pelo nome.
Exibir uma mensagem de confirmação ou erro, conforme o caso.

Sair:

Finaliza a aplicação.

Restrições:

A aplicação deve ser baseada em um menu interativo apresentado ao usuário.

Os nomes devem ter ao menos 3 caracteres e os telefones devem seguir um formato simples, como "99999-9999".

O programa deve validar entradas inválidas e exibir mensagens adequadas.

Não é permitido o uso de banco de dados, apenas armazenamento em memória usando uma lista.

Desafio Extra:

Permitir a exportação da lista de contatos para um arquivo de texto.

Implementar a opção de ordenar a lista de contatos por nome.

*/